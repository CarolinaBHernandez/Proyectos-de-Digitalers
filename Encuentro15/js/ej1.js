/*Crear un array para guardar los nombres de los días de la semana,
empezando por 0 para el domingo. Para comprobar el funcionamiento,
pide al usuario un número entre 0 y 6 y devuelve el nombre del día.


var diasSemana = ['Domingo', 'Lunes', 'Martes', 'Miércoles', 'Jueves', 'Viernes', 'Sábado'];

var numeroUsuario = prompt("Ingresa un número entre 0 y 6:");

var numeroDia = parseInt(numeroUsuario);

if (numeroDia >= 0 && numeroDia <= 6) {
  var nombreDia = diasSemana[numeroDia];
  alert("El día es: " + nombreDia);
} else { 
    alert("El número ingresado no está en el rango (0 al 6).");
}*/